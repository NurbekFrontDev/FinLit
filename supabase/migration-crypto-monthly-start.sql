-- Добавляет колонку start_value_usd, чтобы честно считать доход за месяц:
--   базис = стоимость на начало + пополнение за месяц
--   прибыль/убыток = стоимость на конец - базис
-- Без этой колонки итог искажался: старые вложения засчитывались как доход.
-- Выполнить ОДИН раз в Supabase: SQL Editor -> New query -> вставить и Run.
-- Безопасно запускать повторно (IF NOT EXISTS).
-- =====================================================================

alter table public.crypto_monthly
  add column if not exists start_value_usd numeric(20,2) not null default 0;
